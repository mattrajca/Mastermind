#import "NSColor+Additions.h"

@implementation NSColor (Additions)

+ (NSColor *)darkGreenColor {
	return [NSColor colorWithCalibratedRed:0.1f green:0.6f blue:0.1f alpha:1.0f];
}

+ (NSColor *)reallyLightGreyColor {
	return [NSColor colorWithCalibratedWhite:0.94f alpha:1.0f];
}

@end
